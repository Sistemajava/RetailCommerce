    /**
     * 10-01-2018 Metodo limpiar tabla
     *
     */
    private void limpiarTabla() {
        DefaultTableModel Modelo = (DefaultTableModel) this.tbListaSucursal.getModel();

        int a = Modelo.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            //     System.out.println(" Tabla lista tiene ("+a+") filas, Fila Eliminada :" + i);
            Modelo.removeRow(i);
        }

    }

    /**
     * 15-01-2018 Metodo listar tabla Sucursal
     */
    private void listarTabla() {
        this.tbListaSucursal.removeAll();

        try {
            DefaultTableModel Modelo = (DefaultTableModel) this.tbListaSucursal.getModel();
            Sucursal suc = new Sucursal();

            ArrayList arrList_suc = suc.ListarSucursalJtable();
            Iterator itarrList_suc = arrList_suc.iterator();

            while (itarrList_suc.hasNext()) {
                Sucursal arreglo = (Sucursal) itarrList_suc.next();
                Object[] arreg = new Object[7];

                //aca obtenemos las columnas.
                arreg[0] = arreglo.getSuc_id_suc();
                arreg[1] = arreglo.getSuc_rut_suc();
                arreg[2] = arreglo.getSuc_nom_suc();
                arreg[3] = arreglo.getSuc_id_frm();
                if (arreglo.getSuc_estado() == 'A') {
                    arreg[4] = "Activo";
                } else {
                    arreg[4] = "Inactivo";
                }
                arreg[5] = arreglo.getSuc_id_region();
                arreg[6] = arreglo.getSuc_id_comuna();
                arreg[7] = arreglo.getSuc_nom_dir();
                arreg[8] = arreglo.getSuc_id_gVenta();
                arreg[9] = arreglo.getSuc_fec_alta();
                Modelo.addRow(arreg);
            }

            this.tbListaSucursal.setModel(Modelo);

        } catch (Exception e) {
        }

    }

