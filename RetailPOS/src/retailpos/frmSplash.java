    class TimerListener implements ActionListener {
        @Override
        public void actionPerformed(ActionEvent evt) {
            cont++;
            //jLabel1.setText("nume es :"+cont);
            jProgressBar1.setValue(cont);
            if(cont == 15){ETIQUETA.setText("REVOLUTION SOFTWARE 2018");}
            if(cont == 30){ETIQUETA.setText("Cargando Librerías del Sistema");}
            if(cont == 50){ETIQUETA.setText("Validando Espacio de Memoria Disponible");}
            if(cont == 70){ETIQUETA.setText("Validando Conexión Internet");}
            if(cont == 90){ETIQUETA.setText("Validando Conexión Base de Datos");}
            if(cont == 108){ETIQUETA.setText("Ejecutando Aplicación");}   
            
            
            
                        
            if(cont==120){
                  timer.stop();
                  esconder();
                  
                try {                                      
                     this.marcarConexion();
                                        
                    JFrame frmMenuPrin = new frmMenuPrincipal();
                    //Dimension pantalla = Toolkit.getDefaultToolkit().getScreenSize();
                    //int height = pantalla.height - (pantalla.height * 50)/100;
                    //int width  = pantalla.width  - (pantalla.width * 50)/100;
                    //muestra el menu principal tomando valores de la pantalla
                    //frmMenuPrin.setSize(width, height - 35);                  
                    frmMenuPrin.setLocationRelativeTo(null);
                    frmMenuPrin.setVisible(true);
                    frmMenuPrin.show();
                    
                    
                } catch (SQLException ex) {
                    Logger.getLogger(frmSplash.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                    Logger.getLogger(frmSplash.class.getName()).log(Level.SEVERE, null, ex);
                }                 
            }
        }

        private void marcarConexion() throws Exception {
            Usuario.marcaConexion(Conexion.CONEXION_USERID_OK);
        }
    }
    
    public void esconder(){
        this.setVisible(false);
    }
    

